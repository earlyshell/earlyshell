$(document).ready(function(){
	$('#eh-description-btn').click(function(){
		$('#eh-skills').collapse('hide');
		$('#eh-links').collapse('hide');
		$('#eh-almamater').collapse('hide');
		setTimeout(function(){$('#eh-description').collapse('show');},750);
	});
	$('#eh-skills-btn').click(function(){
		$('#eh-description').collapse('hide');
		$('#eh-links').collapse('hide');
		$('#eh-almamater').collapse('hide');
		setTimeout(function(){$('#eh-skills').collapse('show');},750);
	});
	$('#eh-links-btn').click(function(){
		$('#eh-skills').collapse('hide');
		$('#eh-description').collapse('hide');
		$('#eh-almamater').collapse('hide');
		setTimeout(function(){$('#eh-links').collapse('show');},750);
	});
	$('#eh-almamater-btn').click(function(){
		$('#eh-skills').collapse('hide');
		$('#eh-links').collapse('hide');
		$('#eh-description').collapse('hide');
		setTimeout(function(){$('#eh-almamater').collapse('show');},750);
	});
});